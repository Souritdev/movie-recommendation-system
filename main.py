class TMDBMovieCard(BaseModel):
    """
    Lightweight movie representation used for:
    - Search results
    - Recommendation cards
    - Movie grids
    """

    tmdb_id: int
    title: str
    poster_url: Optional[str] = None
    release_date: Optional[str] = None
    vote_average: Optional[float] = None


class TMDBMovieDetails(BaseModel):
    """
    Detailed movie information returned when
    a user selects a specific movie.
    """

    tmdb_id: int
    title: str
