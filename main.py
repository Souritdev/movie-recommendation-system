def _norm_title(t: str) -> str:
    """
    Normalize a movie title for consistent matching.

    Operations:
    - Convert to string
    - Remove leading/trailing spaces
    - Convert to lowercase

    Example:
        "  Inception  " -> "inception"

    Args:
        t (str): Movie title.

    Returns:
        str: Normalized title.
    """
    return str(t).strip().lower()


def make_img_url(path: Optional[str]) -> Optional[str]:
    """
    Convert a TMDB image path into a complete URL.

    Example:
        Input:
            /abc123.jpg

        Output:
            https://image.tmdb.org/t/p/w500/abc123.jpg

    Args:
        path (Optional[str]): TMDB image path.

    Returns:
        Optional[str]:
            Complete image URL if path exists,
            otherwise None.
    """
    if not path:
        return None

    return f"{TMDB_IMG_500}{path}"


