async def tmdb_get(
    path: str,
    params: Dict[str, Any]
) -> Dict[str, Any]:
    """
    Generic helper function for making GET requests
    to the TMDB API.

    Responsibilities:
    - Add API key automatically
    - Handle network errors
    - Validate response status

    Args:
        path (str):
            TMDB endpoint path.

        params (Dict[str, Any]):
            Query parameters.

    Returns:
        Dict[str, Any]:
            JSON response from TMDB.

    Raises:
        HTTPException:
            502 if TMDB request fails.
    """

    q = dict(params)
    q["api_key"] = TMDB_API_KEY

    try:
        async with httpx.AsyncClient(timeout=20) as client:
            r = await client.get(
                f"{TMDB_BASE}{path}",
                params=q
            )

    except httpx.RequestError as e:
        raise HTTPException(
            status_code=502,
            detail=(
                f"TMDB request error: "
                f"{type(e).__name__} | {repr(e)}"
            ),
        )

    if r.status_code != 200:
        raise HTTPException(
            status_code=502,
            detail=f"TMDB error {r.status_code}: {r.text}",
        )

    return r.json()


async def tmdb_cards_from_results(
    results: List[dict],
    limit: int = 20
) -> List[TMDBMovieCard]:
    """
    Convert raw TMDB search results into
    lightweight movie cards.

    Useful for:
    - Search results
    - Recommendation sections
    - Movie listing pages

    Args:
        results (List[dict]):
            Raw movie objects from TMDB.

        limits (int):
            Maximum number of cards to return.

    Returns:
        List[TMDBMovieCard]:
            Processed movie card objects.
    """

    out: List[TMDBMovieCard] = []

    for m in (results or [])[:limit]:
        out.append(
            TMDBMovieCard(
                tmdb_id=int(m["id"]),
                title=m.get("title")
                or m.get("name")
                or "",
                poster_url=make_img_url(
                    m.get("poster_path")
                ),
                release_date=m.get("release_date"),
                vote_average=m.get("vote_average"),
            )
        )

    return out


async def tmdb_movie_details(
    movie_id: int
) -> TMDBMovieDetails:
    """
    Fetch detailed information about a movie.

    Includes:
    - Overview
    - Poster
    - Backdrop
    - Genres
    - Release date

    Args:
        movie_id (int):
            TMDB movie ID.

    Returns:
        TMDBMovieDetails:
            Detailed movie information.
    """

    data = await tmdb_get(
        f"/movie/{movie_id}",
        {"language": "en-US"}
    )

    return TMDBMovieDetails(
        tmdb_id=int(data["id"]),
        title=data.get("title") or "",
        overview=data.get("overview"),
        release_date=data.get("release_date"),
        poster_url=make_img_url(
            data.get("poster_path")
        ),
        backdrop_url=make_img_url(
            data.get("backdrop_path")
        ),
        genres=data.get("genres", []) or [],
    )


async def tmdb_search_movies(
    query: str,
    page: int = 1
) -> Dict[str, Any]:
    """
    Search TMDB using a movie title or keyword.

    Used by:
    - Search box
    - Suggestions
    - Discovery pages

    Args:
        query (str):
            Movie title or keyword.

        page (int):
            TMDB page number.

    Returns:
        Dict[str, Any]:
            Raw TMDB search response.
    """

    return await tmdb_get(
        "/search/movie",
        {
            "query": query,
            "include_adult": "false",
            "language": "en-US",
            "page": page,
        },
    )


async def tmdb_search_first(
    query: str
) -> Optional[dict]:
    """
    Search TMDB and return the first match.

    This is useful when the user provides
    a movie title and we want the most
    relevant result immediately.

    Args:
        query (str):
            User-entered movie title.

    Returns:
        Optional[dict]:
            First movie result if found,
            otherwise None.
    """

    data = await tmdb_search_movies(
        query=query,
        page=1
    )

    results = data.get("results", [])

    return results[0] if results else None



def build_title_to_idx_map(indices: Any) -> Dict[str,int]:
    """
    indices.pkl can be:
    - dict(title-> index)
    - pandas Series(index = title, value = index)
    We normalise into TITLE_TO_IDX
    """
    title_to_idx: Dict[str,int] = {}

    if isinstance(indices, dict):
        for k , v in indices.items():
            title_to_idx[_norm_title(k)] = int(v)
        return title_to_idx


    # Pandas series or similar mapping
    try:
        for k , v in indices.items():
            title_to_idx[_norm_title(k)] = int(v)
        return title_to_idx
    except Exception:
        # Last resort: if its a list - like etc.
        raise RuntimeError(
            "indices.pkl must be dict or pandas Series-like (with .items())"
        )
    


def get_local_idx_by_title(title:  str) -> int:
    global TITLE_TO_IDX
    if TITLE_TO_IDX is None:
        raise HTTPException(status_code = 500, detail = "TF-IDF index map not initialised")
    key = _norm_title(title)
    if key in TITLE_TO_IDX:
        return int(TITLE_TO_IDX[key])
    raise HTTPException(
        status_code = 404, detail = f"Title not found in local dataset: '{title}' "
    )


def tfidf_recommend_titles(
    query_title: str, top_n: int = 10
) -> List[Tuple[str, float]]:
    """
    Returns list of (title, score) from local df using cosine similarity on TF-IDF matrix.
    Safe against missing columns/rows.
    """
    global df, tfidf_matrix
    if df is None or tfidf_matrix is None:
        raise HTTPException(status_code=500, detail="TF-IDF resources not loaded")

    idx = get_local_idx_by_title(query_title)

    # query vector
    qv = tfidf_matrix[idx]
    scores = (tfidf_matrix @ qv.T).toarray().ravel()

    # sort descending
    order = np.argsort(-scores)

    out: List[Tuple[str, float]] = []
    for i in order:
        if int(i) == int(idx):
            continue
        try:
            title_i = str(df.iloc[int(i)]["title"])
        except Exception:
            continue
        out.append((title_i, float(scores[int(i)])))
        if len(out) >= top_n:
            break
    return out


async def attach_tmdb_card_by_title(title: str) -> Optional[TMDBMovieCard]:
    """
    Uses TMDB search by title to fetch poster for a local title.
    If not found, returns None (never crashes the endpoint).
    """
    try:
        m = await tmdb_search_first(title)
        if not m:
            return None
        return TMDBMovieCard(
            tmdb_id=int(m["id"]),
            title=m.get("title") or title,
            poster_url=make_img_url(m.get("poster_path")),
            release_date=m.get("release_date"),
            vote_average=m.get("vote_average"),
        )
    except Exception:
        return None
    


